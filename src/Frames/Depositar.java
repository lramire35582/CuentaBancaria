    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String sql = "SELECT * FROM  Clientes WHERE ID=?";
        try {
            PreparedStatement st = con.prepareStatement(sql);
            st.setInt(1, globalCliente.identificacion);
            ResultSet rs = st.executeQuery();
            if (rs.next()) {
                int Saldo = rs.getInt("saldo");
                int total = Saldo + Integer.parseInt(Jtcantidad.getText());

                sql = "UPDATE Clientes SET SALDO = ? WHERE ID=?";
                st = con.prepareStatement(sql);
                st.setInt(1, total);
                st.setInt(2, globalCliente.identificacion);
                st.executeUpdate();
                JOptionPane.showMessageDialog(null, "El nuevo saldo es = "+ total );
            }
            
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error en calculo " + ex.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

