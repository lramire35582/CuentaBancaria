    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String id = new String(JID.getText());
        String clav = new String(Jpassword.getPassword());

        try {
            
            String sql = "SELECT * FROM  Clientes WHERE ID=?";
            
            PreparedStatement st = con.prepareStatement(sql);

            st.setInt(1, Integer.parseInt(JID.getText()));

            ResultSet rs = st.executeQuery();
            if (rs.next()) {
                String clv = rs.getString("clave");
                  if (clv.equals(new String(Jpassword.getPassword())))
                  {
                    ClienteBancario obj = new ClienteBancario();
                    obj.setVisible(true);
                    dispose();
                    globalCliente.identificacion = Integer.parseInt(JID.getText());
                  }else{
                  JOptionPane.showMessageDialog(null, "Clave incorrecta");
                  }
            } else {
                JOptionPane.showMessageDialog(null, "Usuario no existe");
                return;
            }


        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Se ha producido un error" + e);
            e.printStackTrace();
        }

    }//GEN-LAST:event_jButton1ActionPerformed

